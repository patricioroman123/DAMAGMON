package pokemon;

public class Stats {
    Integer hp;
    Integer attack;
    Integer defense;
    Integer specialAttack;
    Integer specialDefense;
    Integer speed;

    public Integer getHpStat() {
        return this.hp;
    }

    public Integer getAttackStat() {
        return this.attack;
    }
    
    public Integer getDefenseStat() {
        return this.defense;
    }

    public Integer getSpAttackStat() {
        return this.specialAttack;
    }

    public Integer getSpDefenseStat() {
        return this.specialDefense;
    }

    public Integer getSpeedStat() {
        return this.speed;
    }
}
